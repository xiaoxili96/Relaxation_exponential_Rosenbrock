function [coe1,coe2,coe3,coe4]=coefficient
syms x;
l=((5/4)-(3/2)*x);
a=2*(x-(1/2))*(x-1);
b=(-4)*x*(x-1);
c=2*x*(x-(1/2));

a1=double(int(a*l,0,1));
a2=double(int(b*l,0,1));
a3=double(int(c*l,0,1));  coe1=[a1 a2 a3];
aa1=double(int(a,0,1));
aa2=double(int(b,0,1));
aa3=double(int(c,0,1));  coe2=[aa1 aa2 aa3];
c1=double(int(a^3*l,0,1));
c2=double(int(3*a^2*b*l,0,1));
c3=double(int(3*a^2*c*l,0,1));
c4=double(int(3*a*b^2*l,0,1));
c5=double(int(6*a*b*c*l,0,1));
c6=double(int(3*a*c^2*l,0,1));
c7=double(int(a*l,0,1));
c8=double(int(b^3*l,0,1));
c9=double(int(3*b^2*c*l,0,1));
c10=double(int(3*b*c^2*l,0,1));
c11=double(int(b*l,0,1));
c12=double(int(c^3*l,0,1));
c13=double(int(c*l,0,1));  coe3=[c1 c2 c3 c4 c5 c6 c7 c8 c9 c10 c11 c12 c13];
cc1=double(int(a^3,0,1));
cc2=double(int(3*a^2*b,0,1));
cc3=double(int(3*a^2*c,0,1));
cc4=double(int(3*a*b^2,0,1));
cc5=double(int(6*a*b*c,0,1));
cc6=double(int(3*a*c^2,0,1));
cc7=double(int(a,0,1));
cc8=double(int(b^3,0,1));
cc9=double(int(3*b^2*c,0,1));
cc10=double(int(3*b*c^2,0,1));
cc11=double(int(b,0,1));
cc12=double(int(c^3,0,1));
cc13=double(int(c,0,1));  coe4=[cc1 cc2 cc3 cc4 cc5 cc6 cc7 cc8 cc9 cc10 cc11 cc12 cc13];